package com.skilrock.lms.beans;

public class OlaWalletBean {

	private int walletId;
	private String walletDevName;
	private String walletCategory;
	private String walletDispName;
	private String walletStatus;
	public int getWalletId() {
		return walletId;
	}
	public void setWalletId(int walletId) {
		this.walletId = walletId;
	}
	public String getWalletDevName() {
		return walletDevName;
	}
	public void setWalletDevName(String walletDevName) {
		this.walletDevName = walletDevName;
	}
	public String getWalletCategory() {
		return walletCategory;
	}
	public void setWalletCategory(String walletCategory) {
		this.walletCategory = walletCategory;
	}
	public String getWalletDispName() {
		return walletDispName;
	}
	public void setWalletDispName(String walletDispName) {
		this.walletDispName = walletDispName;
	}
	public String getWalletStatus() {
		return walletStatus;
	}
	public void setWalletStatus(String walletStatus) {
		this.walletStatus = walletStatus;
	}
	
	
}
