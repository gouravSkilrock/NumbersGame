package com.skilrock.lms.rest.services.bean;

public class ResponseDataBean {
	
	private int refUserId;
	private String tpUserId;
	private String userName;
	
	public int getRefUserId() {
		return refUserId;
	}
	public void setRefUserId(int refUserId) {
		this.refUserId = refUserId;
	}
	public String getTpUserId() {
		return tpUserId;
	}
	public void setTpUserId(String tpUserId) {
		this.tpUserId = tpUserId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}

}
